-- Lists of Objects:
stars = {}
comets = {}
shots = {}

function MakeStar() 
 mySpriteChange = 0
 if math.random() < 0.2 then
 	mySpriteChange = math.random(0,3)
	end
	return {x=math.random(0,239), y=math.random(0,135), spdX=(math.random(5,100)/1000), spdY=(math.random(-50,50)/1000), spriteChange=mySpriteChange}
end

function MakeComet(newX,newY,newScale)
 myX=newX
	if myX == nil then
	 myX=x
		while math.abs(myX-x) < minCometDistance do
		 myX=math.random(0,239)
		end
	end
	
	myY=newY
	if myY == nil then
	 myY=y
		while math.abs(myY-y) < minCometDistance do
		 myY=math.random(0,135)
		end
	end
	
	mySprite=math.random(321,325)
	
	myScale=newScale
	if myScale == nil then
	 myScale=math.random(1,3)
	end
	
	myFlip=math.random(0,3)
	
	myRotate=math.random(0,3)
	
	return {alive=true,destroyFinished=false,destroyTime=cometExplosionTime,x=myX,y=myY,sprite=mySprite,spdX=(math.random(-cometSpeed,cometSpeed)/1000),spdY=(math.random(-cometSpeed,cometSpeed)/1000),scale=myScale,flip=myFlip,rotate=myRotate}
end

function MakeShot(myX, myY, myDirection, mySpdX, mySpdY)
 mySprite=305
 if direction==0 or direction==4 then
  mySprite=307
	elseif direction==1 or direction==5 then
	 mySprite=308
	elseif direction==2 or direction==6 then
	 mySprite=305
	else
	 mySprite=306
	end
	
	return {alive=true, despawnTime=shotDespawnTime, sprite=mySprite, x=myX+2, y=myY+2, direction=myDirection, spdX=mySpdX, spdY=mySpdY}
end

function init()
 highscore=pmem(0)
	
 stars[0] = MakeStar()
	for i=0,450 do
	 if math.random() < 0.5 then
		 stars[#stars+1] = MakeStar()
		end
	end
	--comets[0] = MakeComet()
	--for i=1,cometsPerLevel-1 do
	-- comets[#comets+1]=MakeComet()
	--end
	shots[0] = MakeShot(-50,-50,0,0,0)
end

init()

function ResetValues(soft)
 cometsPerLevel=5
 
	if not soft then
		gameOver=false
		gameOverTime=playerExplosionTime+180
		score=0
		highscore=pmem(0)
		mode=0
	end
	t=0
	t_shot=0
	t_menu=0
	if not soft then
		warnColour=false
		isOverspeed=false
		x=120
		y=64
		alive=true
		spdX=0
		spdY=0
		direction=0
		activeSprite=258
		destroyFinished=false
		destroyTime=playerExplosionTime
 end
	nextLevelActive=false
	nextLevelCounter=timeBetweenLevels
	
	-- Lists of Objects:
	for cometNr = 0, #comets do
	 comets[cometNr].alive = false
		comets[cometNr].destroyFinished=true
	end
	for shotNr = 0, #shots do
	 shots[shotNr].alive = false
	end
end

function NextLevel(increaseValue)
 music()
 -- Reset Values
	if increaseValue then
	 ResetValues(true)
	end
	
	-- Increase amount of comets
	if increaseValue then
	 cometsPerLevel=cometsPerLevel+1
	end
	
	-- Spawn comets
	comets[0] = MakeComet()
	for i=1,cometsPerLevel-1 do
	 comets[#comets+1]=MakeComet()
	end
end

function CountComets()
 -- Counts the comets with alive==true
 output=0
	for cometNr=0,#comets do
	 if comets[cometNr].alive then
		 output=output+1
		end
	end
	return output
end

function TIC()
 -- Mode 0 is Menu
 if mode==0 then
	 -- ========= Menu INPUT SECTION =========
		if t_menu>20 and (btn(4) or btn(5) or btn(6) or btn(7)) then
		 mode=1
			NextLevel(false)
		end
		
		-- ========= Menu LOGIC SECTION =========
		-- Handle Background's Stars
		for starNr = 0,#stars do
		 stars[starNr].x = stars[starNr].x + stars[starNr].spdX
			stars[starNr].y = stars[starNr].y + stars[starNr].spdY
			
			if stars[starNr].x < 0 then
			 stars[starNr].x = 239
			elseif stars[starNr].x > 239 then
			 stars[starNr].x = 0
			end
			
			if stars[starNr].y < 0 then
			 stars[starNr].y = 135
			elseif stars[starNr].y > 135 then
			 stars[starNr].y = 0
			end
		end
		
		-- ========= Menu RENDER SECTION =========
		cls(0)
		-- Draw Stars
		for starNr = 0, #stars do
		 spr(508+stars[starNr].spriteChange,stars[starNr].x,stars[starNr].y,0,1)
		end
		
		-- Show Logo
		spr(192,22,25,0,2,0,0,12,2)
		-- Show Text
		--print("Comets", 86, 32, 15, false, 2)
		print("Press A/S/Z/X to begin!", 56, 70, 7, false, 1)
		print("Highscore: " .. highscore, 88, 61, 7, false, 1)
		print("Copyright (C) 2017", 73, 115, 15, false, 1)
		print("Niklas "..apostroph.."Nalquas"..apostroph.." Freund", 58, 122, 15, false, 1)
		print("GNU General Public License Version 3", 19, 129, 15, false, 1)
		print("V"..versionNr, 2, 2, 15, false, 1)
		
		
		-- ========= Menu TIMED LOGIC SECTION =========
		t_menu=t_menu+1
	
	-- Mode 1 is Game	
 elseif mode==1 then
		-- ========= INPUT SECTION =========
		speedAdd=false
		if not gameOver then
			if btn(0) then
			 speedAdd=true
				sfx(1,"D-3",3,0,4,0)
			end
			
			if t%7==0 then
				if btn(2) then
				 direction=direction-1
				elseif btn(3) then
				 direction=direction+1
				end
			end
			
			if t_shot>20 and (btn(4) or btn(5) or btn(6) or btn(7)) then
			 -- SHOOT
				t_shot=0
				shots[#shots+1] = MakeShot(x,y,direction,spdX,spdY)
				sfx(2,"C-5",15,1,12,2)
			end
		end
	 
		-- ========= LOGIC SECTION =========
		-- Handle Background's Stars
		for starNr = 0,#stars do
		 stars[starNr].x = stars[starNr].x + stars[starNr].spdX
			stars[starNr].y = stars[starNr].y + stars[starNr].spdY
			
			if stars[starNr].x < 0 then
			 stars[starNr].x = 239
			elseif stars[starNr].x > 239 then
			 stars[starNr].x = 0
			end
			
			if stars[starNr].y < 0 then
			 stars[starNr].y = 135
			elseif stars[starNr].y > 135 then
			 stars[starNr].y = 0
			end
		end
		
		-- Handle Comets
		for cometNr=0,#comets do
		 if comets[cometNr].alive then
			 comets[cometNr].x=comets[cometNr].x+comets[cometNr].spdX
			 comets[cometNr].y=comets[cometNr].y+comets[cometNr].spdY
				
				negativeBorderCheck= (1-(8*(2^(comets[cometNr].scale-1))))
				
				if comets[cometNr].x > 239 then
				 comets[cometNr].x = negativeBorderCheck
				elseif comets[cometNr].x < negativeBorderCheck then
				 comets[cometNr].x = 239
				end
				
				if comets[cometNr].y > 135 then
				 comets[cometNr].y = negativeBorderCheck
				elseif comets[cometNr].y < negativeBorderCheck then
				 comets[cometNr].y = 135
				end
				
				--Ein Syntaxfehler damit ich weitermachen kann
				if alive then
					if x+3<=comets[cometNr].x+((comets[cometNr].scale*8)-1) and y+3<=comets[cometNr].y+((comets[cometNr].scale*8)-1) and x+3>=comets[cometNr].x and y+3>=comets[cometNr].y then
					 -- If Comet hits Player
						--trace("["..t.."]:Player was hit")
						alive=false
						gameOver=true
						sfx(4,"C-4",35,2,12,0)
						music(0,-1,-1,false)
					end
				end
				
			elseif not comets[cometNr].destroyFinished then
			 if comets[cometNr].destroyTime < 0 then
				 comets[cometNr].destroyFinished=true
				else 
				 if comets[cometNr].destroyTime > ((cometExplosionTime/3)*2) then
					 comets[cometNr].sprite=267
					elseif comets[cometNr].destroyTime > (cometExplosionTime/3) then
					 comets[cometNr].sprite=268
					else
					 comets[cometNr].sprite=269
					end
				 comets[cometNr].destroyTime = comets[cometNr].destroyTime-1
				end
			end
		end
		
		-- Direction sanity checks
		if direction < 0 then
		 direction = 7
		elseif direction > 7 then
		 direction = 0
		end
		
		-- Set sprite for direction
		if direction == 0 then
		 activeSprite=258
		elseif direction == 1 then
		 activeSprite=259
		elseif direction == 2 then
		 activeSprite=275
		elseif direction == 3 then
		 activeSprite=291
		elseif direction == 4 then
		 activeSprite=290
		elseif direction == 5 then
		 activeSprite=289
		elseif direction == 6 then
		 activeSprite=273
		elseif direction == 7 then
		 activeSprite=257
		end
			
		if not gameOver then
			-- Handle Acceleration
			if speedAdd then
			 activeSprite=activeSprite+3
				if direction == 0 then
				 spdY=spdY-playerAcceleration
				elseif direction == 1 then
				 spdX=spdX+(playerAcceleration/2)
					spdY=spdY-(playerAcceleration/2)
				elseif direction == 2 then
				 spdX=spdX+playerAcceleration
				elseif direction == 3 then
				 spdX=spdX+(playerAcceleration/2)
					spdY=spdY+(playerAcceleration/2)
				elseif direction == 4 then
				 spdY=spdY+playerAcceleration
				elseif direction == 5 then
				 spdX=spdX-(playerAcceleration/2)
					spdY=spdY+(playerAcceleration/2)
				elseif direction == 6 then
				 spdX=spdX-playerAcceleration
				elseif direction == 7 then
				 spdX=spdX-(playerAcceleration/2)
					spdY=spdY-(playerAcceleration/2)
				end
			end
		end
		
		-- Handle Player Movement
		x=x+spdX
		y=y+spdY
		if x < -7 then
		 x = 240
		elseif x > 240 then
		 x = -7
		end
		if y < -7 then
		 y = 136
		elseif y > 136 then
		 y = -7
		end
			
		-- Player's Inertia Dampeners
		spdX = spdX/400*399
		spdY = spdY/400*399
		
		if not gameOver then
			-- Show Overspeed if Player is too fast (Not really required)
			if math.abs(spdX)+math.abs(spdY) > playerMaxSpeed then
			 isOverspeed=true
			else
			 isOverspeed=false
			end
		else 
		 if destroyTime > ((playerExplosionTime/7)*6) then
			 activeSprite=263
			elseif destroyTime > ((playerExplosionTime/7)*5) then
			 activeSprite=264
			elseif destroyTime > ((playerExplosionTime/7)*4) then
			 activeSprite=265
			elseif destroyTime > ((playerExplosionTime/7)*3) then
			 activeSprite=266
			elseif destroyTime > ((playerExplosionTime/7)*2) then
			 activeSprite=267
			else
			 activeSprite=268
			end
			destroyTime=destroyTime-1
		end
		
		-- Handle Shots
		for shotNr = 0,#shots do
		 if shots[shotNr].alive then
			 -- Shot Movement
			 if shots[shotNr].direction == 0 then
				 shots[shotNr].y = shots[shotNr].y - playerShotSpeed +shots[shotNr].spdY
				elseif shots[shotNr].direction == 4 then
				 shots[shotNr].y = shots[shotNr].y + playerShotSpeed +shots[shotNr].spdY
				elseif shots[shotNr].direction == 2 then
				 shots[shotNr].x = shots[shotNr].x + playerShotSpeed +shots[shotNr].spdX
				elseif shots[shotNr].direction == 6 then
				 shots[shotNr].x = shots[shotNr].x - playerShotSpeed +shots[shotNr].spdX
				elseif shots[shotNr].direction == 1 then
				 shots[shotNr].y = shots[shotNr].y - (playerShotSpeed/2) +shots[shotNr].spdY
					shots[shotNr].x = shots[shotNr].x + (playerShotSpeed/2) +shots[shotNr].spdX
				elseif shots[shotNr].direction == 5 then
				 shots[shotNr].y = shots[shotNr].y + (playerShotSpeed/2) +shots[shotNr].spdY
					shots[shotNr].x = shots[shotNr].x - (playerShotSpeed/2) +shots[shotNr].spdX
				elseif shots[shotNr].direction == 3 then
				 shots[shotNr].y = shots[shotNr].y + (playerShotSpeed/2) +shots[shotNr].spdY
					shots[shotNr].x = shots[shotNr].x + (playerShotSpeed/2) +shots[shotNr].spdX
				elseif shots[shotNr].direction == 7 then
				 shots[shotNr].y = shots[shotNr].y - (playerShotSpeed/2) +shots[shotNr].spdY
					shots[shotNr].x = shots[shotNr].x - (playerShotSpeed/2) +shots[shotNr].spdX
				end
				
				-- Sanity Checks
				if shots[shotNr].x < -4 or shots[shotNr].x > 239 or shots[shotNr].y < -4 or shots[shotNr].y > 135 then
				 shots[shotNr].alive = false
				end
				
				if not gameOver then
					-- Handle Shot Collisions
					for cometNr=0,#comets do
					 if comets[cometNr].alive then
							if shots[shotNr].x+2 <= (comets[cometNr].x+((comets[cometNr].scale*8)-1)) and shots[shotNr].y+2 <= (comets[cometNr].y+((comets[cometNr].scale*8)-1)) and shots[shotNr].x+2 >= comets[cometNr].x and shots[shotNr].y+2 >= comets[cometNr].y then
							 -- Runs if collided:
								score = score + 1
								comets[cometNr].alive = false
								if not (comets[cometNr].scale <= 1) then
								 -- Split comet
									comets[#comets+1]=MakeComet(comets[cometNr].x+(((comets[cometNr].scale*8)/2)-1),comets[cometNr].y+(((comets[cometNr].scale*8)/2)-1),comets[cometNr].scale-1)
								 comets[#comets+1]=MakeComet(comets[cometNr].x+(((comets[cometNr].scale*8)/2)-1),comets[cometNr].y+(((comets[cometNr].scale*8)/2)-1),comets[cometNr].scale-1)
								end
								shots[shotNr].alive = false
								sfx(3,"G-3",15,2,11,2)
							end
						end
					end
				end
				
				if shots[shotNr].despawnTime<=0 then
				 shots[shotNr].alive=false
				end
				
				shots[shotNr].despawnTime=shots[shotNr].despawnTime-1
			end
		end
		
		if not gameOver then
		 -- Check if all comets are destroyed
			if CountComets() <= 0 then
			 if not nextLevelActive then
				 music(1,-1,-1,false)
					nextLevelActive=true
				end
				nextLevelCounter=nextLevelCounter-1
				if nextLevelCounter <= 0 then
 			 NextLevel(true)
				end
			end
		end
		
		-- ========= RENDER SECTION =========
		cls(0)
		
		-- Draw Stars
		for starNr = 0, #stars do
		 spr(508+stars[starNr].spriteChange,stars[starNr].x,stars[starNr].y,0,1)
		end
		
		-- Draw Shots
		for shotNr = 0, #shots do
		 if shots[shotNr].alive then
		  spr(shots[shotNr].sprite, shots[shotNr].x, shots[shotNr].y, 0, 1)
			end
		end
		
		-- Draw Player
		if (not gameOver) or destroyTime >= 0 then
		 spr(activeSprite,x,y,15,1)
		end
		
		-- Draw Comets
		for cometNr=0,#comets do
		 if comets[cometNr].alive or not comets[cometNr].destroyFinished then
			 spr(comets[cometNr].sprite, comets[cometNr].x, comets[cometNr].y, 0, comets[cometNr].scale, comets[cometNr].flip, comets[cometNr].rotate)
			end
		end
		
		-- Show Score
		print("Score: "..score,2,2)
		print("Highscore: "..highscore,2,10)
		
		-- Show Overspeed
		if t%10==0 then warnColour = not warnColour end
		overSpdColour=6
		if warnColour then
		 overSpdColour=15
		end
		if gameOver then
		 print("GAME OVER!",64,64,overSpdColour,false,2)
		elseif isOverspeed then
		 print("OVERSPEED!",64,64,overSpdColour,false,2)
		end
		
		-- Debug:
		--print("X="..spdX.."; Y="..spdY,84,129,14)
		--print(#stars,70,70)
		--print(math.abs(spdX)+math.abs(spdY),50,50,15)
		--print(CountComets(),50,50)
		
		-- ========= TIMED LOGIC SECTION =========
		t=t+1
		t_shot=t_shot+1
		if gameOver then
		 if gameOverTime <= 0 then
			 -- Go back to menu
				if score >= highscore then
				 pmem(0,score)
				end
				ResetValues(false)
			end
			gameOverTime=gameOverTime-1
		end
	end
end

